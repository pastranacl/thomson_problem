/*****************************************************************************************/
/*                                  reverse                                              */
/* inverse the direction of the string s in place                                        */
/*****************************************************************************************/
void reverse(char s[])
 {
     int i, j;
     char c;

     for (i = 0, j = strlen(s)-1; i<j; i++, j--) {
         c = s[i];
         s[i] = s[j];
         s[j] = c;
     }
}  


/*****************************************************************************************/
/*                                  itoa                                                 */
/* itoa converts the integer to the smae number in a char array                          */
/*****************************************************************************************/

 void itoa(int n, char s[])
 {
     int i, sign;

     if ((sign = n) < 0)  /* record sign */
         n = -n;          /* make n positive */
     i = 0;
     do {       /* generate digits in reverse order */
         s[i++] = n % 10 + '0';   /* get next digit */
     } while ((n /= 10) > 0);     /* delete it */
     if (sign < 0)
         s[i++] = '-';
     s[i] = '\0';
     reverse(s);
}




