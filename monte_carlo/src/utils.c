/*****************************************************************************************/
/*                                      dmin                                             */
/* Index of the maximum value in the double vector input as v.                           */
/*****************************************************************************************/
int dmin(double *v, int n)
{
    double minv=v[0];
    int idx=0;
    for(int i=1; i<n; i++){
        if( v[i] < minv ){
            minv=v[i];
            idx=i;    
        }
    }
    return idx;
}
/*****************************************************************************************/
/*                                      dmax                                             */
/* Index of the maximum value in the double vector input as v.                           */
/*****************************************************************************************/
int dmax(double *v, int n)
{
    double maxv=v[0];
    int idx=0;
    for(int i=1; i<n; i++){
        if( v[i] > maxv ){
            maxv=v[i];
            idx=i;    
        }
    }
    return idx;
}




